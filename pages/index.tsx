import { Box } from "@chakra-ui/react";
import type { NextPage } from "next";
import Head from "next/head";
import { useEffect, useState } from "react";
import {
  getExtraToppingInfoList,
  getInfoList,
  getSandwichInfoList,
} from "../api/apiRequests";
import HomeScreen from "../components/home/Home";
import SplashScreen from "../components/splash/SplashScreen";
import TitleHeader from "../components/TitleHeader";
import {
  filterExtraSandwich,
  filterToppings,
} from "../service/exception.service";
import { GridCategoryTitleList, InfoType, SizeList } from "../types/const";

export type DEFAULT_SANDWICH_INFO_TYPE = {
  bread: InfoType[];
  sandwich: InfoType[];
  veggies: InfoType[];
  size: InfoType[];
  cheese: InfoType[];
  extraCheese: InfoType[];
  sauces: InfoType[];
  extras: InfoType[];
};

const DEFAULT_SANDWICH_INFO: DEFAULT_SANDWICH_INFO_TYPE = {
  bread: [],
  sandwich: [],
  veggies: [],
  size: [],
  cheese: [],
  extraCheese: [],
  sauces: [],
  extras: [],
};

const Home: NextPage = () => {
  const [api, setApi] = useState(false);
  setTimeout(() => {
    setApi(true);
  }, 1800);

  const [sandwichInfo, setSandwichInfo] = useState(DEFAULT_SANDWICH_INFO);

  const divideItemFunc = async (category: string) => {
    if (category === "sandwich") {
      const res = await getSandwichInfoList(category);
      setSandwichInfo({ ...sandwichInfo, sandwich: res });
    }
    if (category === "size") {
      const res = SizeList;
      setSandwichInfo({ ...sandwichInfo, size: res });
    }
    if (category === "bread") {
      const res = await getInfoList(category);
      setSandwichInfo({ ...sandwichInfo, bread: res });
    }
    if (category === "veggies") {
      const res = await getInfoList("vegetable");
      setSandwichInfo({ ...sandwichInfo, veggies: res });
    }
    if (category === "cheese") {
      const res = await getInfoList(category);
      setSandwichInfo({ ...sandwichInfo, cheese: res });
    }
    if (category === "extra cheese") {
      const res = await getInfoList(category);
      setSandwichInfo({ ...sandwichInfo, extraCheese: res });
    }
    if (category === "sauces") {
      const res = await getInfoList("sauce");
      setSandwichInfo({ ...sandwichInfo, sauces: res });
    }
    if (category === "extras") {
      const toppingArray = await getExtraToppingInfoList("extras");
      const sandwichArray = await getSandwichInfoList("extras");
      const res = [
        ...filterToppings(toppingArray),
        ...filterExtraSandwich(sandwichArray),
      ];
      setSandwichInfo({ ...sandwichInfo, extras: res });
    }
  };

  const handleInfo = async () => {
    GridCategoryTitleList.map(async (title) => {
      await divideItemFunc(title.toLowerCase());
    });
  };

  useEffect(() => {
    handleInfo();
  }, []);

  return (
    <>
      <Head>
        <title>Sub Kcalculator</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <Box
        border={"1px solid red"}
        left={0}
        right={0}
        bottom={0}
        top={0}
        bg="Grayscale.10"
        display="flex"
        justifyContent="center"
      >
        <Box id="maxW box" bg="Grayscale.10" h="full" w="100%" maxW={"516px"}>
          <TitleHeader />
          <Box>
            {api ? (
              <HomeScreen sandwichInfo={sandwichInfo} />
            ) : (
              <SplashScreen />
            )}
          </Box>
        </Box>
      </Box>
    </>
  );
};

export default Home;

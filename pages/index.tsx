import { Box } from "@chakra-ui/react";
import type { NextPage } from "next";
import Head from "next/head";
import { useEffect, useState } from "react";
import {
  getExtraToppingInfoList,
  getInfoList,
  getSandwichInfoList,
} from "../api/apiRequests";
import HomeScreen from "../components/home/Home";
import SplashScreen from "../components/splash/SplashScreen";
import TitleHeader from "../components/TitleHeader";
import {
  filterExtraSandwich,
  filterToppings,
} from "../service/exception.service";
import { GridCategoryTitleList, InfoType, SizeList } from "../types/const";

export type DEFAULT_SANDWICH_INFO_TYPE = {
  bread: InfoType[];
  sandwich: InfoType[];
  veggies: InfoType[];
  size: InfoType[];
  cheese: InfoType[];
  extraCheese: InfoType[];
  sauces: InfoType[];
  extras: InfoType[];
};
const DEFAULT_SANDWICH_INFO: DEFAULT_SANDWICH_INFO_TYPE = {
  bread: [],
  sandwich: [],
  veggies: [],
  size: [],
  cheese: [],
  extraCheese: [],
  sauces: [],
  extras: [],
};

export async function getStaticProps() {
  const sandwich: DEFAULT_SANDWICH_INFO_TYPE = DEFAULT_SANDWICH_INFO;
  console.log("===sandwich===", sandwich);
  const divideItemFunc = async (category: string) => {
    if (category === "sandwich") {
      const res = await getSandwichInfoList(category);
      sandwich.sandwich = res;
    }
    if (category === "size") {
      sandwich.size = SizeList;
    }
    if (category === "bread") {
      const res = await getInfoList(category);
      sandwich.bread = res;
    }
    if (category === "veggies") {
      const res = await getInfoList("vegetable");
      sandwich.veggies = res;
    }
    if (category === "cheese") {
      const res = await getInfoList(category);
      sandwich.cheese = res;
    }
    if (category === "extra cheese") {
      const res = await getInfoList(category);
      sandwich.extraCheese = res;
    }
    if (category === "sauces") {
      const res = await getInfoList("sauce");
      sandwich.sauces = res;
    }
    if (category === "extras") {
      const toppingArray = await getExtraToppingInfoList("extras");
      const sandwichArray = await getSandwichInfoList("extras");
      const res = [
        ...filterToppings(toppingArray),
        ...filterExtraSandwich(sandwichArray),
      ];
      sandwich.extras = res;
    }
  };

  await Promise.all(
    GridCategoryTitleList.map(async (title) => {
      await divideItemFunc(title.toLowerCase());
    })
  );

  return {
    props: {
      sandwich,
    },
  };
}

interface HomeProps {
  sandwich: DEFAULT_SANDWICH_INFO_TYPE;
}

const Home: NextPage<HomeProps> = ({ sandwich }) => {
  const [api, setApi] = useState(false);
  setTimeout(() => {
    setApi(true);
  }, 1800);

  return (
    <>
      <Head>
        <title>Sub Kcalculator</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <Box
        left={0}
        right={0}
        bottom={0}
        top={0}
        bg="Grayscale.10"
        display="flex"
        justifyContent="center"
      >
        <Box id="maxW box" bg="Grayscale.10" h="full" w="100%" maxW={"516px"}>
          <TitleHeader />
          <Box>
            {api ? <HomeScreen sandwichInfo={sandwich} /> : <SplashScreen />}
          </Box>
        </Box>
      </Box>
    </>
  );
};
export default Home;
